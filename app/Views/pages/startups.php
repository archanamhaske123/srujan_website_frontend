<script>
function openPopup(title, subtitle, image, socialLinks) {
  console.log('Popup function called:', title, subtitle, image, socialLinks); // Debugging log
  document.getElementById('popup-title').textContent = title;
  document.getElementById('popup-subtitle').textContent = subtitle;
  document.getElementById('popup-image').src = image;

  // Generate social media icons dynamically
  const socialIconsContainer = document.getElementById('social-icons');
  socialIconsContainer.innerHTML = ''; // Clear previous content
  socialLinks.forEach(link => {
    const aTag = document.createElement('a');
    aTag.href = link.url;
    aTag.target = '_blank';
    aTag.innerHTML = `<img src="${link.icon}" alt="${link.name}" class="img-fluid">`;
    socialIconsContainer.appendChild(aTag);
  });

  document.getElementById('popup').style.display = 'flex';
}

function closePopup() {
  document.getElementById('popup').style.display = 'none';
}

</script>
